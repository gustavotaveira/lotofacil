public class Numero {

    private int valor;
    private int frequencia;

    public Numero() {
    }

    public Numero(int valor, int frequencia) {
        this.valor = valor;
        this.frequencia = frequencia;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public int getFrequencia() {
        return frequencia;
    }

    public void setFrequencia(int frequencia) {
        this.frequencia = frequencia;
    }


}
